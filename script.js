function drawChart() {
    // ダークモード判定用
    const isDarkMode = window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches;
    const textColor = isDarkMode ? '#e0e0e0' : '#666';
    const gridColor = isDarkMode ? 'rgba(255, 255, 255, 0.2)' : 'rgba(0, 0, 0, 0.1)';

    const ctx = categoryChartEl.getContext('2d');
    const data = {
        labels: Object.values(categoryNames),
        datasets:[{
            label: '分野別正答率',
            data: Object.keys(categoryScores).map(key => {
                return categoryTotals[key] > 0 ? (categoryScores[key] / categoryTotals[key]) * 100 : 0;
            }),
            backgroundColor: isDarkMode ? 'rgba(102, 178, 255, 0.4)' : 'rgba(0, 123, 255, 0.2)',
            borderColor: isDarkMode ? 'rgba(102, 178, 255, 1)' : 'rgba(0, 123, 255, 1)',
            borderWidth: 2,
            pointBackgroundColor: isDarkMode ? 'rgba(102, 178, 255, 1)' : 'rgba(0, 123, 255, 1)',
            pointRadius: 4,
        }]
    };

    myChart = new Chart(ctx, {
        type: 'radar',
        data: data,
        options: {
            scales: {
                r: {
                    angleLines: {
                        display: true,
                        color: gridColor
                    },
                    grid: {
                        color: gridColor
                    },
                    suggestedMin: 0,
                    suggestedMax: 100,
                    ticks: {
                        stepSize: 20,
                        backdropColor: 'transparent',
                        color: textColor
                    },
                    pointLabels: {
                        font: {
                            size: 14
                        },
                        color: textColor
                    }
                }
            },
            plugins: {
                legend: {
                    position: 'top',
                    labels: {
                        color: textColor
                    }
                }
            }
        }
    });
}